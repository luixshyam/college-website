				The Mathematics Department started its functioning since the inception of the college known as Jorhat Science College in the year 1971, which was the only general degree college with degree courses only under Govt. of Assam. Initially classes were held in Jorhat Engineering College till the construction of College buildings in its present site, where the classes started from 1976. It may be mentioned that Honours (Major) was compulsory from the beginning of the college.

Mathematics Department introduced M.Sc. Course under CBCS mode from the academic session 2016-17 affiliated to Dibrugarh University. Before that from 2007, Department is teaching three papers of Mathematics in three semesters in 3 year B.Sc.(I.T.) Course and from 2009, six papers of Mathematics in five different semesters of 4 year B.E. Course . Now, all the courses viz, M.Sc., B.Sc., and B.Sc.(IT) are under ASTU from the 2018-19 session onward. Moreover Ph.D. programme in Mathematics started from the session 2018-19 under Assam Science and Technology University, Guwahati, Assam.

Prof. Tarakeswar Choudhury was the founder teacher of the department appointed by Govt. of Assam on deputation from Cotton College, Guwahati. Thereafter quite a good number of dedicated teachers served this department for the promotion of Mathematics education in this region. Sincere efforts are still continuing for the cause of Mathematics.

The department has its own computer laboratory under the financial assistance of Science and Technology Department, Govt. of India. Apart from academics, the department has been organizing yearly Mathematical Olympiad and Mathematics competition under Assam Academy of Mathematics.
